# This file contain the different types of quotes in python

single_quotes = 'This is a string with single quotes'
double_quotes = "This is a string with double quotes"
triple_single_quotes = '''This is a string with triple single quotes'''

# Basically all three types of quotes are used to define string in python
triple_double_quotes = """This is a string with triple double quotes"""
# Triple quotes are often used for multi-line strings or docstrings

print(single_quotes)
print(double_quotes)
print(triple_single_quotes)
print(triple_double_quotes)