# -- Problem : Check if number is positive  --
num = int(input("Enter Any Number : "))

if num > 0 :
    print(f"Number {num} is positive.")

if num < 0 :
    print(f"Number {num} is Negative.")

if num == 0 :
    print(f"Number {num} is Zero.")