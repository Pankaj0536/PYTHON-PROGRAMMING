# In this file we will learn various datatpes in python like (int , float , string , etc)

num1 = 34               # integer datatype
num2 = 45.78            # float datatype
num3 = 10.5e100         # float with 'e' that indicate power '10'
name = 'Pankaj Rana'    # string datatype
is_student = True       # boolean datatype
complex_num = 2 + 3j    #complex datatype

# type() this is a function which tells us the datatype of a variable

print(type(num1))
print(type(num2))
print(type(num3))
print(type(name))
print(type(is_student))
print(type(complex_num))

# output look like 

# <class 'int'>
# <class 'float'>
# <class 'float'>
# <class 'str'>
# <class 'bool'>
# <class 'complex'>