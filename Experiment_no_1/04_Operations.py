# This file contain some arithmetic operations in python

print("\n--- Math Operations ---")
a = 8
b = 5

add = a + b # 8 + 5 = 13 
sub = a - b # 8 - 5 = 3 
mul = a * b # 8 X 5 = 40 
div = a / b         # Division (float result)   8 / 5 = 1.6
floor_div = a // b  # Floor division
mod = a % b         # Remainder
power = a ** b      # Exponentiation

print("Addition:", add)
print("Subtraction:", sub)
print("Multiplication:", mul)
print("Division:", div)
print("Floor Division:", floor_div)
print("Modulus:", mod)
print("Exponentiation:", power)

# output : 
# --- Math Operations ---
# Addition: 13      
# Subtraction: 3    
# Multiplication: 40
# Division: 1.6     
# Floor Division: 1
# Modulus: 3
