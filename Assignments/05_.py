# Convert the following list into a dictionary where elements are keys and their length is the value: ['apple', 'banana', 'kiwi']

# Given List
fruit_list = ['apple', 'banana', 'kiwi']

# Dictionary conversion 
fruit_dict = {
    'apple' : len('apple'),
    'banana' : len('banana'),
    'kiwi' : len('kiwi')
}

for fruit in fruit_dict :
    print(fruit)

# output :
# apple
# banana
# kiwi
