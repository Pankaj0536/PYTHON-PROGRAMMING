# Write Python code to print numbers from 2 to 10 with a step size of 2 using the range() function.

# Syntax for range() :
#   range(start , end , step )
#   here , start is included , wherares end is not included 
#   step is the jump like i++ (for sigle step , step = 1 or dont write )

for i in range(2 , 11 , 2) : 
    print(i , end = " ")

# output :
# 2 4 6 8 10 