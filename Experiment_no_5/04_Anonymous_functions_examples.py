# In this file we will run some Anonymous functions examples

add_num = lambda a, b: a + b
multiply_num = lambda a , b : a * b
divide_num = lambda a , b : a/b 
power_calculate = lambda a , b : a**b

print(f" 23 + 45 = {add_num(23 , 45)}")
print(f" 7 * 8 = {multiply_num(7 , 8)} ")
print(f" 80 / 8 = {divide_num(7 , 8)} ")
print(f" 9 ^ 3 = {power_calculate(9 , 3)} ")


